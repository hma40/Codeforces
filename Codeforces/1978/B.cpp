int n,a,b;
int profit(int k) {
    if(k<0) return 0;
    if(k>n) return 0;
    if(k>b) return 0;
    return a*(n-k)+(b-k+1+b)*k/2;
}
signed main() {
    ios_base::sync_with_stdio(false); 
    cin.tie(0);
    int t;
    cin >> t;
    while(t--) {

        cin >> n >> a >> b;
        int minDer = (2*b-2*a+1)/2;
        cout << max({profit(0), profit(n), profit(b), profit(minDer), profit(minDer-1), profit(minDer-2), profit(minDer+1), profit(minDer+2)}) << endl;
    }
    return 0;
}