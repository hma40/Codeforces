signed main() {
    ios_base::sync_with_stdio(false); 
    cin.tie(0);
    // freopen("input.txt" , "r" , stdin);
    // freopen("output.txt" , "w", stdout);
    int t = 1;
    cin >> t;
    while(t--) {
        int lo = 2, hi = 999;
        /*
        ? a b (a<=b):
        a*b: ans<a
        (a+1)*b: a<=ans<b
        (a+1)*(b+1): b<=ans
        if hi=lo+2, we can determine answer with a single query: lo+1,hi
        */
        int queriesLeft = 7;
        while(lo+2<hi) {
            int mid0 = (2*lo+hi)/3;
            int mid1 = (lo+2*hi)/3;
            assert(queriesLeft>0);
            queriesLeft--;
            int ac = mid0*mid1, wa1 = mid0*(1+mid1);
            int qu = query(mid0,mid1);
            if(qu==ac) {
                //ans is bigger than mid1
                lo=mid1+1;
            } else if(qu==wa1) {
                //ans is at most mid1 but more than mid0
                lo=mid0+1;
                hi=mid1;
            } else {
                //ans is at most mid0
                hi=mid0;
            }
        }
        if(lo==hi) cout << "! " << lo << endl;
        else if(hi==lo+1) {
            assert(queriesLeft>0);
            if(query(lo,lo)==lo*lo) {
                cout << "! " << hi << endl;
            } else {
                cout << "! " << lo << endl;
            }
        } else {
            assert(queriesLeft>0);
            int qu = query(lo+1,hi);
            if(qu==(lo+1)*hi) {
                cout << "! " << lo << endl;
            } else if(qu==(lo+1)*(1+hi)) {
                cout << "! " << lo+1 << endl;
            } else {
                cout << "! " << hi << endl;
            }
        }
    }
    return 0;
}