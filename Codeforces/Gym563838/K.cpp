set<pair<double,double>> intervals;
void add(pair<double,double> p) {
    auto ub = intervals.upper_bound(p), lb = intervals.lower_bound(p);
    --lb;
    if(p.s>(*ub).f) {
        p.s=max(p.s,(*ub).s);
        p.f=min(p.f,(*ub).f);
        intervals.erase(ub);
    }
    if(p.f<(*lb).s) {
        p.f=min(p.f,(*lb).f);
        p.s=max(p.s,(*lb).s);
        intervals.erase(lb);
    }
    intervals.insert(p);
}
