bool poss(int qual, int n, int k) {
    //n-qual-1
    int last = n-qual-1;
    if(last<0) return true;
    int first = n%(qual+1);
    int terms = (last-first)/(qual+1)+1;
    int time = n+(first+last)*terms/2;
    return time<=k;
}
signed main() {
    ios_base::sync_with_stdio(false); 
    cin.tie(0);
    int t;
    cin >> t;
    while(t--) {
        int n,k;
        cin >> n >> k;
        int lo = 0, hi = 1e10;
        while(lo+1<hi) {
            int mid = (lo+hi)/2;
            if(poss(mid,n,k)) {
                hi=mid;
            } else {
                lo=mid;
            }
        }
        if(poss(lo,n,k)) cout << lo << endl;
        else cout << hi << endl;
    }
    return 0;
}